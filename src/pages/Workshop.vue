        <h2>Description</h2>

        <p>
          Knowledge translation is a core part of being an effective feminist
          researcher and advocate. It helps ensure that the information produced
          by feminist research is used to support evidence-based decision
          making, policy making, influencing, and social change. The ability to
          describe your knowledge translation strategy is also increasingly a
          requirement of research funding agencies, including the Social Science
          and Humanities Research Council.
        </p>

        <p>
          In this 3-hour online workshop, graduate students will learn about the
          practice of feminist knowledge translation and explore strategies to
          communicate research for maximum impact, uptake, and use by target
          audiences. A key aspect of feminist knoweldge translation is returning
          knowledge generated back to the communities that we work with, and
          ensuring its applicability and use for those who helped create it.
        </p>

        <h2>Learning Objectives</h2>

        <p>By the end of the workshop, participants will understand how to:</p>

