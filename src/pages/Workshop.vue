            design a feminist knowledge translation strategy for their research
          </li>
          <li>
            promote the uptake and use of their work by communities and target
            audiences
          </li>
          <li>
            think through practical and ethical issues in knowledge production
            and dissemination (e.g. impact, ownership, accessibility, power
            relations)
          </li>
          <li>
            produce different types of knowledge products and leverage them
            effectively
          </li>
          <li>evaluate the reach and impact of their work</li>
        </ul>

        <h2>Workshop Design</h2>

        <p>
          This workshop is designed for graduate students in feminist studies,
